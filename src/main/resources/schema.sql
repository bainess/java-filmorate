CREATE TABLE IF NOT EXISTS ratings (
    id INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    mpa_name VARCHAR(20) NOT NULL UNIQUE
);

CREATE TABLE IF NOT EXISTS genres (
    id INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name VARCHAR(25) NOT NULL UNIQUE
);

CREATE TABLE IF NOT EXISTS films (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name VARCHAR(50) NOT NULL,
    description VARCHAR(250) NOT NULL DEFAULT '',
    release_date TIMESTAMP,
    duration INT NOT NULL,
    mpa_id INT REFERENCES ratings(id)
);

CREATE TABLE IF NOT EXISTS users (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    login VARCHAR(20) UNIQUE,
    email VARCHAR(30) UNIQUE,
    name VARCHAR(30),
    birthday TIMESTAMP
);

-- Используем составной ключ вместо лишнего id
CREATE TABLE IF NOT EXISTS films_genre (
    film_id BIGINT NOT NULL REFERENCES films(id) ON DELETE CASCADE,
    genre_id INT NOT NULL REFERENCES genres(id) ON DELETE CASCADE,
    PRIMARY KEY (film_id, genre_id)
);

CREATE TABLE IF NOT EXISTS film_likes (
    film_id BIGINT NOT NULL REFERENCES films(id) ON DELETE CASCADE,
    user_id BIGINT NOT NULL REFERENCES users(id) ON DELETE CASCADE,
    PRIMARY KEY (film_id, user_id)
);

CREATE TABLE IF NOT EXISTS user_friends (
    user_id BIGINT NOT NULL REFERENCES users(id) ON DELETE CASCADE,
    friend_id BIGINT NOT NULL REFERENCES users(id) ON DELETE CASCADE,
    PRIMARY KEY (user_id, friend_id)
);

CREATE TABLE IF NOT EXISTS reviews (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    content TEXT NOT NULL,
    is_positive BOOLEAN NOT NULL,
    user_id BIGINT REFERENCES users(id) ON DELETE CASCADE,
    film_id BIGINT REFERENCES films(id) ON DELETE CASCADE
);

CREATE TABLE IF NOT EXISTS review_useful (
    review_id BIGINT REFERENCES reviews(id) ON DELETE CASCADE,
    user_id BIGINT REFERENCES users(id) ON DELETE CASCADE,
    is_like BOOLEAN,
    PRIMARY KEY (review_id, user_id)
);

CREATE TABLE IF NOT EXISTS event_types (
    id INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    event_name VARCHAR(25) NOT NULL
);

CREATE TABLE IF NOT EXISTS event_operations (
    id INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    operation_name VARCHAR(25) NOT NULL
);

CREATE TABLE IF NOT EXISTS events (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    ts TIMESTAMP,
    user_id BIGINT REFERENCES users(id) ON DELETE CASCADE,
    event_type_id INT REFERENCES event_types(id),
    operation_id INT REFERENCES event_operations(id),
    entity_id BIGINT NOT NULL
);

CREATE TABLE IF NOT EXISTS directors (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    director_name VARCHAR(75) NOT NULL
);

CREATE TABLE IF NOT EXISTS film_directors (
    director_id BIGINT REFERENCES directors(id) ON DELETE CASCADE,
    film_id BIGINT REFERENCES films(id) ON DELETE CASCADE,
    PRIMARY KEY (director_id, film_id)
);